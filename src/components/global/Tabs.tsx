function useOnScreen(ref: MutableRefObject<HTMLDivElement | null>) {
  const [isOnScreen, setIsOnScreen] = useState(false);
  const observerRef = useRef<IntersectionObserver>(null);

  useEffect(() => {
    // @ts-ignore
    observerRef.current = new IntersectionObserver(
      ([entry]) => setIsOnScreen(entry.isIntersecting),
      {
        rootMargin: "0px",
        threshold: 1.0,
      },
    );
  }, []);

  useEffect(() => {
    observerRef.current?.observe(ref.current!);

    return () => {
      observerRef.current?.disconnect();
    };
  }, [ref]);

  return isOnScreen;
}

const KeyFeaturesSection = ({
  items,
  featureIndex,
  setSelectedTab,
}: {
  items: { title: string; description: string; Icon: JSX.Element }[];
  featureIndex: number;
  setSelectedTab: (index: number) => void;
}) => {
  const elementRef = useRef(null);
  const isOnScreen = useOnScreen(elementRef);

  useEffect(() => {
    isOnScreen && setSelectedTab(featureIndex);

    return () => {};
  }, [isOnScreen, setSelectedTab, featureIndex]);

  return (
    <div
      ref={elementRef}
      className="grid grid-cols-1 gap-x-6 gap-y-10 md:grid-cols-2"
    >
      {items.map((item, index) => (
        <div key={index} className="flex flex-col gap-6 md:flex-row">
          <div className="bg-iconBg flex h-[72px] min-w-[72px] items-center justify-center rounded-full">
            {item.Icon}
          </div>
          <div className="space-y-4">
            <h6 className="text-2xl font-semibold text-textHeader">
              {item.title}
            </h6>
            <P>{item.description}</P>
          </div>
        </div>
      ))}
    </div>
  );
};

